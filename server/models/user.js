const mongoose = require('mongoose');// import mongoose

const userShema = mongoose.Schema({
    name: {
        type: String,
        trim: true,
        required: true
    },
    email: {
        type: String,
        trim: true,
        required: true,
        unique: true
    },
    password: {
        type: String,
        required: true
    },

})

module.exports = mongoose.model('User', userShema);